package com.entity;

import java.io.Serializable;
import java.util.Date;
import javax.persistence.Basic;
import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.FetchType;
import javax.persistence.Id;
import javax.persistence.JoinColumn;
import javax.persistence.ManyToOne;
import javax.persistence.NamedQueries;
import javax.persistence.NamedQuery;
import javax.persistence.Table;
import javax.persistence.Temporal;
import javax.persistence.TemporalType;

/**
 *
 * @author Marco Andrade
 */
@Entity
@Table(name = "LIINVENTARIO")
public class Liinventario implements Serializable {
    private static final long serialVersionUID = 1L;
    @Basic(optional = false)
    @Column(name = "INVPRODUCTO")
    private String invproducto;
    @Basic(optional = false)
    @Column(name = "INVINICIAL")
    private Integer invinicial;
    @Basic(optional = false)
    @Column(name = "INVREAL")
    private Integer invreal;
    @Basic(optional = false)
    @Column(name = "INVRESEVADO")
    private Integer invresevado;
    @Id
    @Basic(optional = false)
    @Column(name = "INVCLAVE")
    private Integer invclave;
    @Column(name = "INVFECHA")
    @Temporal(TemporalType.DATE)
    private Date invfecha;
    @Basic(optional = false)
    @Column(name = "INVMIN")
    private Integer invmin;
    @Basic(optional = false)
    @Column(name = "INVMAX")
    private Integer invmax;
    @Basic(optional = false)
    @Column(name = "INVPR")
    private Integer invpr;
    @Basic(optional = false)
    @Column(name = "INVSMIN")
    private Integer invsmin;
    @Basic(optional = false)
    @Column(name = "INVSMAX")
    private Integer invsmax;
    @Basic(optional = false)
    @Column(name = "INVSPR")
    private Integer invspr;
    @JoinColumn(name = "INVALMACEN", referencedColumnName = "ALMCLAVE")
    @ManyToOne(optional = false, fetch = FetchType.LAZY)
    private Lialmacenes invalmacen;
    @JoinColumn(name = "INVEMPRESA", referencedColumnName = "EMPCLAVE")
    @ManyToOne(optional = false, fetch = FetchType.LAZY)
    private Liempresas invempresa;

    public Liinventario() {
    }

    public Liinventario(Integer invclave) {
        this.invclave = invclave;
    }

    public Liinventario(Integer invclave, String invproducto, Integer invinicial, Integer invreal, Integer invresevado, Integer invmin, Integer invmax, Integer invpr, Integer invsmin, Integer invsmax, Integer invspr) {
        this.invclave = invclave;
        this.invproducto = invproducto;
        this.invinicial = invinicial;
        this.invreal = invreal;
        this.invresevado = invresevado;
        this.invmin = invmin;
        this.invmax = invmax;
        this.invpr = invpr;
        this.invsmin = invsmin;
        this.invsmax = invsmax;
        this.invspr = invspr;
    }

    public String getInvproducto() {
        return invproducto;
    }

    public void setInvproducto(String invproducto) {
        this.invproducto = invproducto;
    }

    public long getInvinicial() {
        return invinicial;
    }

    public void setInvinicial(Integer invinicial) {
        this.invinicial = invinicial;
    }

    public Integer getInvreal() {
        return invreal;
    }

    public void setInvreal(Integer invreal) {
        this.invreal = invreal;
    }

    public Integer getInvresevado() {
        return invresevado;
    }

    public void setInvresevado(Integer invresevado) {
        this.invresevado = invresevado;
    }

    public Integer getInvclave() {
        return invclave;
    }

    public void setInvclave(Integer invclave) {
        this.invclave = invclave;
    }

    public Date getInvfecha() {
        return invfecha;
    }

    public void setInvfecha(Date invfecha) {
        this.invfecha = invfecha;
    }

    public Integer getInvmin() {
        return invmin;
    }

    public void setInvmin(Integer invmin) {
        this.invmin = invmin;
    }

    public Integer getInvmax() {
        return invmax;
    }

    public void setInvmax(Integer invmax) {
        this.invmax = invmax;
    }

    public Integer getInvpr() {
        return invpr;
    }

    public void setInvpr(Integer invpr) {
        this.invpr = invpr;
    }

    public Integer getInvsmin() {
        return invsmin;
    }

    public void setInvsmin(Integer invsmin) {
        this.invsmin = invsmin;
    }

    public Integer getInvsmax() {
        return invsmax;
    }

    public void setInvsmax(Integer invsmax) {
        this.invsmax = invsmax;
    }

    public Integer getInvspr() {
        return invspr;
    }

    public void setInvspr(Integer invspr) {
        this.invspr = invspr;
    }

    public Lialmacenes getInvalmacen() {
        return invalmacen;
    }

    public void setInvalmacen(Lialmacenes invalmacen) {
        this.invalmacen = invalmacen;
    }

    public Liempresas getInvempresa() {
        return invempresa;
    }

    public void setInvempresa(Liempresas invempresa) {
        this.invempresa = invempresa;
    }

    @Override
    public int hashCode() {
        int hash = 0;
        hash += (invclave != null ? invclave.hashCode() : 0);
        return hash;
    }

    @Override
    public boolean equals(Object object) {
        // TODO: Warning - this method won't work in the case the id fields are not set
        if (!(object instanceof Liinventario)) {
            return false;
        }
        Liinventario other = (Liinventario) object;
        if ((this.invclave == null && other.invclave != null) || (this.invclave != null && !this.invclave.equals(other.invclave))) {
            return false;
        }
        return true;
    }

    @Override
    public String toString() {
        return "com.entity.Liinventario[invclave=" + invclave + "]";
    }

}
